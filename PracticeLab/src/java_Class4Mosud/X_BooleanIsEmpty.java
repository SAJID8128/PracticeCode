package java_Class4Mosud;

public class X_BooleanIsEmpty {

	public static void main(String[] args) {
		
		//empty string
		String str1 = "";			
		//prints true
		System.out.println(str1.isEmpty());
		
		
		//non-empty string
		String str2 = "hello";
		//prints false
		System.out.println(str2.isEmpty());
		
	}

}
